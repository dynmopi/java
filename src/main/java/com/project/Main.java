package com.project;
// 1
// java é caseSensitive também.
// Main e main são diferentes.

// a class, deve começar em letra maiúscula. E o nome do arquivo, deve corresponder
// ao nome da classe
public class Main {

    // o método main é a primeira instrução que o programa irá compilar.
    public static void main (String[] args) {
        // println, é o método para imprimir, assim como o print que não pula uma
        // linha.
        System.out.println("Olá mundo");
        System.out.print("Estou aprendendo o Java ");
        System.out.print("println e print são diferentes.");

    }// as chaves servem para marcar o início e o fim dos codigos.
}